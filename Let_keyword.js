// In JavaScript, there are three keywords
//  that can be used to declare variables: let
//  , var, and const. Let declares a block-scoped 
//  local variable, optionally initializing it to a value,
//   while var declarations are globally scoped or function scoped

let first_name = "Aksh";
console.log(first_name);

// let first_name ="Deepu";
first_name = "Nitin";
console.log(first_name);

// We can not redeclare the let  variable
// but we can change the value ;